public class Calculadora {

    public double add(double op1, double op2){
        return op1 + op2;
    }

    public double sub(double op1, double op2){
        return op1 - op2;
    }

    public double mult(double op1, double op2){
        return op1 * op2;
    }

    public double div(double op1, double op2){
        return op1 / op2;
    }
}
